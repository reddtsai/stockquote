CREATE SCHEMA `STOCK` DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci;

CREATE TABLE `STOCK`.`DIVIDEND` (
  `CODE` int NOT NULL,
  `DATE` varchar(10) NOT NULL,
  `NAME` varchar(20) CHARACTER SET utf8 COLLATE utf8_general_ci DEFAULT NULL,
  `PE` decimal(8,2) DEFAULT NULL,
  `PB` decimal(8,2) DEFAULT NULL,
  `YIELD` decimal(8,2) DEFAULT NULL,
  `YEAR` varchar(10) DEFAULT NULL,
  `FISCAL` varchar(10) DEFAULT NULL,
  `CREATED` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `UPDATED` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`CODE`,`DATE`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;